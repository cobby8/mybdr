// ─────────────────────────────────────────────────────────────────────────────
// 정규식
// ─────────────────────────────────────────────────────────────────────────────

/**
 * 한국 휴대폰/일반전화 (하이픈/점/공백/없음 구분자 허용).
 *
 * 매칭 예:
 *   010-1234-5678  /  010.1234.5678  /  010 1234 5678  /  01012345678
 *   011-234-5678   /  02-1234-5678   (일반전화 02 국번도 범위 안에 들어올 수 있음)
 *
 * 왜 `\b` 경계를 쓰는가:
 *   본문에 "가입 010-...", "코트 010..." 처럼 앞에 한글이 붙는 케이스도 흔해서
 *   `\b`(단어 경계)가 전/후 비영숫자 전환에서 매칭돼 안전하다.
 *   한글-숫자 전환도 `\b` 경계로 인식된다.
 */
const PHONE_REGEX = /\b(01[016789])[-.\s]?(\d{3,4})[-.\s]?(\d{4})\b/g;

/**
 * 한국 계좌번호 패턴 2종.
 *
 * [A] 하이픈 포함형 — 2~4개 그룹, 각 그룹 1~8자리. 국내 시중은행 거의 대부분 커버.
 *   매칭 예:
 *     110-123-456789      (국민 3그룹 12자리)
 *     3333-01-1234567     (카카오뱅크 3그룹 13자리)
 *     1002-123-456789     (우리)
 *
 * [B] 하이픈 없는 연속형 — 10~14자리 순수 숫자.
 *   매칭 예:
 *     1234567890        (10자리)
 *     12345678901234    (14자리)
 *
 * 왜 10~14자리인가:
 *   국내 시중은행 계좌번호는 보통 10~14자리. 9자리 이하는 오탐 위험 (대회 ID 5자리 등).
 *   하이픈 제거한 **순수 숫자 길이**로 최종 필터링해서 "1234567" 같은 짧은 숫자는 스킵.
 *
 * 중요:
 *   전화번호(010-XXXX-XXXX = 11자리)와 패턴이 겹치므로
 *   **반드시 phone 먼저 치환 → account 나중에** 순서로 적용해야 이중 변환 방지.
 *
 * 왜 두 패턴을 | 로 합치는가:
 *   하이픈형을 먼저 시도하고, 실패 시 연속형으로 fallback. alternation 순서가 중요.
 *   (정규식 엔진은 왼쪽부터 greedy 매칭)
 */
const ACCOUNT_REGEX =
  /\b(?:\d{1,6}(?:-\d{1,8}){1,3}|\d{10,14})\b/g;

// ─────────────────────────────────────────────────────────────────────────────
// 공개 API
// ─────────────────────────────────────────────────────────────────────────────

/**
 * 전화번호만 마스킹.
 *   010-1234-5678  →  010-****-****
 *   010.1234.5678  →  010-****-****  (구분자도 "-"로 정규화)
 *   01012345678    →  010-****-****
 */
export function maskPhoneNumbers(text: string): string {
  if (!text) return text;
  // prefix만 남기고 나머지는 ****-**** 고정 (구분자 통일)
  return text.replace(PHONE_REGEX, (_m, prefix: string) => `${prefix}-****-****`);
}

/**
 * 계좌번호만 마스킹. **phone 치환 후에** 호출해야 안전.
 *
 *   110-123-456789  →  110-****-****
 *   1234567890      →  123-****-****  (10자리도 마스킹)
 *   12345           →  (변경 없음, 5자리는 계좌가 아니라고 판단)
 */
export function maskAccountNumbers(text: string): string {
  if (!text) return text;
  return text.replace(ACCOUNT_REGEX, (match) => {
    // 하이픈 제거 후 순수 숫자 길이 검사
    const digitsOnly = match.replace(/-/g, "");
    // 10자리 미만 → 계좌 아닐 확률 높음 (대회 ID/우편번호/일반 숫자) → 원본 유지
    // 14자리 초과 → 일반 계좌 범위 밖 (주민번호/카드번호는 별도 처리 영역) → 원본 유지
    if (digitsOnly.length < 10 || digitsOnly.length > 14) return match;
    // 앞 3자리만 남기고 마스킹 (은행 식별 코드 3자리 관례)
    return `${digitsOnly.slice(0, 3)}-****-****`;
  });
}

/**
 * 전화 + 계좌 둘 다 마스킹 (통합 API).
 *
 * 왜 **phone 먼저**:
 *   "010-1234-5678"는 하이픈 제거 시 11자리 숫자 → ACCOUNT_REGEX 에도 매칭되어
 *   이중 치환되거나 "101-****-****"처럼 잘못 잘릴 수 있다.
 *   phone을 먼저 `010-****-****`로 바꾸면 이후 계좌 정규식에
 *   **이미 문자(`*`)가 섞여 있어** 매칭되지 않는다.
 */
export function maskPersonalInfo(text: string): string {
  if (!text) return text;
  return maskAccountNumbers(maskPhoneNumbers(text));
}
